// Cálculo simples subtração
#include <stdio.h>
int main()
{
    int numero1, numero2, subtracao;
    printf("Digite o primeiro numero: ");
    scanf("%d", &numero1);
    printf("Digite o segundo numero: ");
    scanf("%d", &numero2);
    subtracao = numero1 - numero2;
    printf("A resultado dos numeros é: %d ", subtracao);
    printf("\n\n");
}
